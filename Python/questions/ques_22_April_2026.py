class TreeNode:
    def __init__(self, key=0):
        self.key = key
        self.left = None
        self.right = None


@print_return_value
def range_sum_bst(root: TreeNode, low: int, high: int) -> int:
    if root is None:
        return 0
    q = deque([root])
    total = 0
    while q:
        node = q.popleft()
        if low <= node.key <= high:
            total += node.key
        if node.left and node.key > low:
            q.append(node.left)
        if node.right and node.key < high:
            q.append(node.right)
        
    return total


